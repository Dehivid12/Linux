# Crear lista de archivos
echo "Hola Linux" > archivo.txt     # Crea el archivo "archivo.txt" y escribe "Hola Linux" 

cat archivo.txt                      # Muestra el contenido de "archivo.txt" en pantalla

cp archivo.txt copia.txt             # Copia "archivo.txt" y crea un nuevo archivo llamado "copia.txt"

ls -l                                # Hace una lista de los archivos del directorio en formato detallado

rm archivo.txt                       # Elimina el archivo "archivo.txt"

